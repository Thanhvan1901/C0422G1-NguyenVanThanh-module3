package model.person.Employee;

public class Education_degree {
    private int id ;
    private String name ;

    public Education_degree() {
    }

    public Education_degree(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
